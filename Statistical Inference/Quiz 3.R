
Question 1
In a population of interest, a sample of 9 men yielded a sample average brain volume of 1,100cc 
and a standard deviation of 30cc. What is a 95% Students T confidence interval for the mean brain 
volume in this new population?
[1077,1123]
[1092, 1108]
[1080, 1120]
[1031, 1169]

Answer: qt(0.975, df=8)*30/3 = [1077,1123]

